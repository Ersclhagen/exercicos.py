# Exercício 04 - Média simples
# Peça para o usuário digitar três notas e calcule a média entre elas.

numero1 = int(input("Digite um número: "))
numero2 = int(input("Digite um número: "))
numero3 = int(input("Digite um número: "))
media = (numero1 + numero2 + numero3) / 3

print(f"A média dos números {numero1}, {numero2}, {numero3} é a média: {media:.1f} ")