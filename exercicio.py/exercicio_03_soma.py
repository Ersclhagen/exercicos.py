# Exercicio 03 - Soma de números
# Peça para o usuário digitar dois números.
# Some os dois valores e exiba o resultado com uma frase, como:
# "A soma de 4 e 6 é igual a 10"

primeiro_numero = int(input("Digite o primeiro número:"))
segundo_numero = int(input("Digite o segundo número:"))

soma = primeiro_numero + segundo_numero 

print(f"A soma de {primeiro_numero} e {segundo_numero} é igual a {soma}")

